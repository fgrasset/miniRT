#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	int		i;
	char	*ptr;

	len = ft_strlen(s1) + 1;
	i = 0;
	ptr = malloc(len * sizeof(char));
	if (ptr == (NULL))
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
